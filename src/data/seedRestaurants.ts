// 將種子數據轉換為 Supabase 格式
export function convertToSupabaseFormat(restaurant: SeedRestaurant) {
  return {
    restaurant_id: `${restaurant.name.toLowerCase().replace(/\s+/g, "-")}_${restaurant.location.toLowerCase().replace(/\s+/g, "-")}`,
    name: restaurant.name,
    location: restaurant.location,
    web2_score: restaurant.web2_score,
    truth_score: restaurant.truth_score,
    bot_probability: restaurant.bot_probability,
    confidence: restaurant.confidence,
    status: restaurant.status,
    verification_count: restaurant.verified_reports,
    analysis_summary: restaurant.analysis_summary,
    key_findings: restaurant.key_findings,
    evidence_items: [
      {
        type: "ai_analysis",
        title: "AI 鑑識分析",
        description: restaurant.analysis_summary,
      },
      {
        type: "tags",
        title: "標籤",
        description: restaurant.tags.join(", "),
      },
    ],
    created_at: new Date().toISOString(),
    updated_at: new Date().toISOString(),
    last_audit_at: new Date().toISOString(),
  };
}
